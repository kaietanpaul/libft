#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t			i;
	unsigned char	*nstr;

	i = 0;
	nstr = str;
	while (i < n)
	{
		nstr[i] = '\0';
		i++;
	}
}
